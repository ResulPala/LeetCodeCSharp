public class SolutionTwo
{
    public int[] TwoSum(int[] numbers, int target)
    {
        for (int i = 0; i < numbers.Length; i++)
        {
            for (int j = 0; j < numbers.Length; j++)
            {
                if (numbers[i] + numbers[j] == target && i != j)
                    return new int[] { i, j };
            }


        }

        return null;
    }
